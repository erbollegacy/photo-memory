'use strict'

import { app, BrowserWindow, protocol } from 'electron'
import sharp from 'sharp'
import queryString from 'query-string'
/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\')
}

let mainWindow
const winURL = process.env.NODE_ENV === 'development'
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow () {
  /**
   * Initial window options
   */
  mainWindow = new BrowserWindow({
    height: 563,
    useContentSize: true,
    width: 1000,
    webPreferences: {
      nodeIntegration: true
    }
  })

  mainWindow.loadURL(winURL)

  mainWindow.on('closed', () => {
    mainWindow = null
  })
}

app.on('ready', createWindow)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

app.on('ready', () => {
  console.log('registering thumb protocol')
  protocol.registerBufferProtocol('thumb', (request, callbacks) => {
    const [ url, query ] = request.url.substr(6).split('?')
    const { w } = queryString.parse(query)
    const width = parseInt(w || 260)

    sharp(url)
      .resize(width)
      .jpeg({ quality: 100 })
      .toBuffer()
      .then(data => {
        callbacks({ mimeType: 'image/jpeg', data })
      })
  })
})

